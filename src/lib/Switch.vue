<template>
  <button @click="toggle" :class="{checked}">
<!--    如果x为true，button class是checked；反之则不是checked-->
    <span></span>
  </button>
</template>
<script lang="ts">
import {ref} from 'vue'
  export default {
    setup(){
      const checked = ref(false)
      const toggle = () => {
        checked.value = !checked.value
      }
      return {checked, toggle}
    }
  }
</script>

<style lang="scss" scoped>
$h: 22px;
$h2: $h - 4px;
button{
  background: grey;
  height: $h;
  width: $h*2;
  border: none;
  border-radius: $h/2;
  position: relative;
}
span{
  position: absolute;
  top: 2px;
  left: 2px;
  height: $h2;
  width: $h2;
  background:lightgreen;
  border-radius: $h2 / 2;
}
button.checked{
  background: white;
}
button.checked > span {
  left: calc(100% - #{$h2} - 2px);
}
</style>